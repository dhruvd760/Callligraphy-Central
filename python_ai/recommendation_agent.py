    def recommend_posts(self, recommendation_type: str = "trending", filters: Dict[str, Any] = None) -> Dict[str, Any]:
        """
        Main orchestration method for the RecommendationAgent.
        
        Routes the recommendation request to the appropriate specific method based on 
        recommendation_type, and formats the output response matching the expected schema.

        Args:
            recommendation_type (str): Type of suggestion (style, tags, similar, user, trending).
            filters (Dict[str, Any]): Search constraints (e.g. style name, tags list, post_id).

        Returns:
            Dict[str, Any]: A structured response including recommendations (also alias results),
                            type, and count.
        """
        try:
            logger.info(f"Orchestrating recommendations for type: {recommendation_type}")
            recommendations = []
            
            if filters is None:
                filters = {}

            # Route to the appropriate recommendation strategy
            if recommendation_type == "style" and "style" in filters:
                recommendations = self.recommend_by_style(filters["style"])
            elif recommendation_type == "tags" and "tags" in filters:
                recommendations = self.recommend_by_tags(filters["tags"])
            elif recommendation_type == "similar" and "post_id" in filters:
                recommendations = self.recommend_similar_posts(filters["post_id"])
            elif recommendation_type == "user" and "user_id" in filters:
                recommendations = self.recommend_for_user(filters["user_id"])
            else:
                # Default to trending
                recommendations = self.recommend_trending()

            # Construct the final response dictionary supporting both results and recommendations key
            response = {
                "recommendation_type": recommendation_type,
                "recommendations": recommendations,
                "results": recommendations,  # Backward compatibility alias
                "count": len(recommendations)
            }
            return response
            
        except Exception as e:
            logger.error(f"Error in recommend_posts orchestration: {e}", exc_info=True)
            # Return a graceful fallback dictionary
            return {
                "recommendation_type": recommendation_type,
                "recommendations": [],
                "results": [],
                "count": 0
            }
