                "Assess their visual or technical popularity and explain what other scribes can learn from them. "
                "Respond ONLY with a JSON array of objects, where each object has these exact keys:\n"
                "  \"id\": [integer],\n"
                "  \"trend_score\": [integer between 90 and 100],\n"
                "  \"reason\": [explanation of why this piece is popular and what calligraphers can learn from it]\n"
