<h1>What is Calligraphy?</h1>
<p>
    Calligraphy is the art of beautifully crafted lettering, where handwriting 
    transforms into an expressive and elegant form of visual design. More than 
    just writing, calligraphy combines creativity, precision, and rhythm to shape
    letters in a way that enhances their aesthetic appeal. Artists often use different techniques to create patterns, flourishes
    and unique designs.
</p>
<p>
    In earlier times it was used to create manuscripts, religious texts, and official documents. Whether used for 
    artistic expression, historical manuscripts, or modern design, calligraphy is
    a unique blend of tradition and innovation that continues to captivate people
    across cultures.
</p>

<br><br><hr><br>

<h1>A brief history of Calligraphy</h1>    
<p>
    Long before the invention of printing, handwritten texts were the primary
    means of recording information. Calligraphy played a central role in making
    documents visually appealing and legible.
</p>

<p>Many cultures have contributed to the evolution of calligraphy:</p><br>
